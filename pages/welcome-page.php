          <p>Use this page for sending mails to commenters. On this page you find the email addresses of all commenters listed with a check box option. If you don't want to mail any particular commenter(s), you can uncheck the email(s) on this list. After you selected the commenters, click on the email subject you first created and press the Send Mail button. <a href="<?php echo WP_eemail_FAV; ?>" target="_blank" class="helplink"><em>click here for more info</em></a></p>
          <strong>Send Mail to Subscribed Users</strong>
          <p>This plugin offers a new option. It's a widget that you can drag to your sidebar. It serves as a subscribe option for your site visitors. Use this page to send mails to visitors who subscribed to your newsletter through the widget. You find them all listed with a check box option. If you don't want to mail any particular subscriber(s), you can uncheck the email(s) on this list. After you selected the subscribers, click on the email subject you first created and press the Send Mail button. <a href="<?php echo WP_eemail_FAV; ?>" target="_blank" class="helplink"><em>click here for more info</em></a></p>
          <p>You can see all subscribers on the 'View Subscriber' page.</p>
          <strong>Send Mail to Users who Contacted You</strong>
          <p>This plugin now comes with a 'Simple Contact Form'. I thought it would be useful to combine this feature with the newsletter plugin. After installing the contact form, your site visitors may contact you using this form. Those users then are visible to you on this page, and you can email them by using the same procedure as outlined before. <a href="http://www.gopiplus.com/work/2010/07/18/simple-contact-form/" target="_blank" class="helplink"><em>click here for more info</em></a></p>
          <p>Install the plugin 'Simple Contact Form' on your site. If it is not useful to you, omit this menu.</p>
          <strong>Export Users to CSV</strong>
          <p>This is a new option that allows you to download all users into a formatted text file (CSV) for later import on another site of yours, or for using particular emails of it in your ordinary mail client, or for migrating your Wordpress site to another server. <a href="<?php echo WP_eemail_FAV; ?>" target="_blank" class="helplink"><em>click here for more info</em></a></p>
